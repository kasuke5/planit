<h4>" PLAN'IT, le premier gestionnaire de vols ! "</h4><br><br>

<h5>
	Plan'IT est une application qui a été développé par une équipe de jeunes étudiants.<p><br>
	Cette application nous est venue à l'idée suite aux crashs d'avions annoncés aux informations.<p>
	
	Plan’ IT est une application au fonctionnement simple et accessible quelque soit le niveau en informatique.<p>
	Elle permet de retrouver facilement et rapidement les informations enregistrées en temps réelles et ainsi de <p> 
	pouvoir gérer le plus efficacement possible les vols suivant les aéroports.<p>
	Elle s’adresse principalement aux professionnels c’est-à-dire aux internes des aéroports. Ils pourront avoir accès <p> 
	en toute liberté, grâce à un statut qui leur ai dédié, à toutes les données des différents vols sur le site.<p>
	Les statuts correspondent à des droits différents suivant les utilisateurs. <p>
	Ils permettent de limiter tout allée et venue sur le site.<p>
	Les informations sont stockées dans une base de données sécurisées où seuls les administrateurs en ont l’accès. <p>
	Ces derniers peuvent modifier, ajouter ou supprimer des vols ou des passagers.<p>
	En conclusion, Plan’IT est l’Application qui vous permettra de gagner du temps !
</h5>

<h3><br>Comment ça marche ?</h3>
Pour pouvoir faire une recherche de manière efficace:<br>
il vous suffit, dans un premier temps, de vous inscrire pour avoir accès à la zone de recherche<br>
dans un second temps, dirigez vous vers l'onglet de Recherche.<br>
Dans cet onglet, vous avez un formulaire qui contient plusieurs champs qui vous laisse un large choix de recherche,<p>
rentrez les informations que vous possédez et cliquez sur Valider.<br>
Vous obtiendrez des Résultats suivant votre recherche !

